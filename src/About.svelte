<p>
	<b>Your data never leaves your computer.</b> This is a pure client-side webapp, all data is loaded in your browser, there is no backend. This means that I don't have access to the book you're writing, which also means that you're responsible to use the <i>Save/Load</i> features to backup your gamebook to disk. GWP tries it best to always save your gamebooks to local storage. Saving to disk is a good practice to keep your books safe.
