<h1>About Gamebook Webby Press</h1>
<p>
	This is a minimal web app to help you create gamebooks for the <a href="https://play.date" target="_blank">Playdate</a> handheld console by <a href="https://panic.com" target="_blank">Panic</a>. The source-code generated by <i>Gamebook.Webby.Press</i> is compatible with the <a href="https://play.date/dev/" target="_blank">Playdate SDK</a> and can be compiled out-of-the-box if you have everything set correctly on your machine.
</p>
